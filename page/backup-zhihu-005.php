<p>但从科学研究和统计得到的数据来看，题主的“<b>未成年人还不成熟，性别这种人生大事要等到成年以后有判断能力以后再决定</b>”的思维<b>如果贯彻应用到现实中，将会导致更多的跨性别青少年因为自杀而死亡，也会让更多的成年跨性别者因为没有得到及时的激素替代治疗而患上抑郁症/双相情感障碍/边缘人格障碍/精神分裂症等各种精神疾病</b><br/></p>
<p>首先需要明确的第一点科学事实是：<b>绝大多数性别焦虑持续时间超过两年以上的跨性别青少年，终生都会保持其性别认同（终生不会后悔）</b></p>
<p>按照美国ucsf的跨性别中心的跨性别青少年的报告数据显示：当一个跨性别青少年性别焦虑的持续时间达到2年以上时，其在接下来终生的时间内不会后悔的人数比例在80％以上，如果性别焦虑持续时间达到3/4/5年，其接下来终生时间不会后悔的比例在90％/97％/99％以上，<b>并且即使在极少数的情况下出现后悔情况，其对人生生活质量水平，心理健康，死亡率的负面影响也远远小于一个终生不会后悔的跨性别者无法得到及时治疗的负面影响</b></p>
<p><b>如果一个跨性别者要等到成年以后才进行干预治疗，其终生将会面临比一般人群高出数百倍的自杀风险和抑郁风险，并且因为生理原因面临严重的性别焦虑</b></p>
<p>其次需要明确的第二点科学事实是：<b>在一个青少年进入青春期以后（一般在11岁以后），性别认同一旦形成，无法因为任何强制性的外力原因而改变，如果对其进行“矫正治疗”，在99.7％以上的情况下不仅无法成功“矫正”，而且会加重其性别焦虑，导致在86.1％以上的情况下让该青少年患上/加重精神疾病，从而让该青少年的自杀率比普通人群高出700倍以上</b></p>
<p>“性别认同一旦形成无法因为任何强制性的外力因素而改变”已经成为国际医学的公认真理和标准答案，<b>历史上所有尝试矫正跨性别性别认同的尝试案例均和尝试矫正同性恋性别取向的案例一样无一例外的失败了</b></p>
<p>即使是在相对封闭保守的大陆地区，你也会从任何一个三甲医生那里得到“性别认同无法心理矫正”这个答案</p>
<p>那这时候题主可能要问了：我就是担心那极少数会后悔的青少年怎么办，我是一个家长，我不希望我的孩子变性，哪怕只有一丝一毫的可能我也要让他做个“正常的男孩子”/也要让她做个“正常的女孩子”，即使冒着TA很可能会抑郁一辈子甚至直接死掉的风险我也希望TA成为一个“正常人”</p>
<p>医学上已经为了防止“后悔”的情况给出了有效的干预手段，那就是“青春期阻滞剂”，该阻滞剂的效果是暂缓跨性别者青春期的第二性征发育，从而达到<b>缓解性别焦虑，降低自杀风险，给出犹豫时间的目的</b>，这样即使孩子后悔了，只要停止服用“青春期阻滞剂”，会依然按照原来的生理性别发育</p>
<p>但是“青春期阻滞剂”仅适用于尚未完成青春期发育以前，成年以后如果要缓解性别焦虑需要进行激素替代治疗手段</p>
<p>如果您家孩子，性别焦虑时间已经超过了两年时间，并且已经成年，为了您家孩子未来一辈子的身心健康和幸福</p>
