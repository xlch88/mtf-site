<p><b>什么人更容易得抑郁症？这个问题没有明确的答案，但以下均为抑郁症高发人群：</b></p>
<p>第一，如果一个人的父母、子女或同父母的兄弟姐妹中有人得过抑郁症，那么这个人得抑郁症的可能性比其他人要高出2~10倍。</p>
<p>第二，经历过创伤性事件的人，比如性侵、霸凌、自然灾害、意外事故等。</p>
<p>第三，性格比较内向、敏感、悲观、自卑的人。</p>
<p>第四，长期处于压力和紧张的氛围中，并且情绪无法得到疏解的人。</p>
<p>第五，有其他严重的疾病尤其是脑部疾病的人。</p>
<p><b>很多抑郁症患者会有自杀的想法，但这绝对不是因为他们意志薄弱或想要博得关注，而是因为决策能力下降、行事容易冲动，或者被情绪反复及躯体症状折磨得生不如死。</b></p>

<div class="block">
	抑郁症想自杀是生理原因导致的，不是真实想法，如果患上抑郁症，你的大脑会产生脑部病变，停止分泌多巴胺。即使你的现实生活很美满，很幸福，你也会想自杀。<br/>
	假设，你在明知道你自杀会给你爱人造成打击的情况下，并且现实中没有任何让你不顺心的事情，但只要你患有抑郁症，你就会选择自杀<br/>
	抑郁症的典型特征：没有理由的情绪低落，没有理由的想死。<br/>
	即使有理由，大部分情况下也是非常小的理由，例如：今天被一个人嘲笑了，所以我还是自杀吧，死了就不会有人嘲笑我了。<br/>
	抑郁症的自杀思维途径是：抑郁症→无缘无故想自杀→寻找自杀的理由→自杀<br/>
	普通人的自杀思维路径则是<br/>
	遭受挫折→因为挫折想自杀→自杀<br/>
	所以抑郁症必须就医，不是靠“安慰一下”“打开心结”就能解决的。<br/>
	是脑子出问题了，神经递质罢工了，多巴胺不分泌了，根本原因不是心理上的问题<br/>
	治好抑郁症，需要心理治疗＋吃精神药＋物理治疗。
</div>

<p><b>每个人都有求生本能，当这一本能被扑面而来的绝望感淹没，逼你放弃自己的生命时，希望你能够做到以下几点：</b></p>
<ol>
	<li>不要独处，尽量去人多的地方。</li>
	<li>找一些事情转移自己的注意力，不要一直陷在“自杀”这件事里。</li>
	<li>如果还是无法摆脱自杀的冲动，就去找一个你信任的人，让他寸步不离地陪伴你。</li>
	<li>如果以上方法都无法打消你的自杀念头，就必须去医院了，通过住院或者电休克疗法快速摒除自杀的冲动。</li>
</ol>

<p><b>一个人无论何时具备以下危险因素中的4~5项，都可被视为处于自杀的高风险期：</b></p>
<ul>
	<li>有自杀家族史。</li>
	<li>有自杀未遂史。</li>
	<li>已经制订了具体的自杀计划。</li>
	<li>最近经历了心爱之人去世、离婚或分居。</li>
	<li>最近由于经济损失或受虐而导致家庭不稳定。</li>
	<li>陷入巨大的创伤而难以自拔。</li>
	<li>有精神疾病。</li>
	<li>有药物或酒精滥用史。</li>
	<li>最近遭受了躯体或心理创伤。</li>
	<li>有失败的医疗史。</li>
	<li>独居，而且与他人失去联系。</li>
	<li>有抑郁症，目前处于抑郁症的恢复期或正在住院治疗。</li>
	<li>在分配个人财产或安排后事。</li>
	<li>有特别的情绪和行为改变，比如冷漠、退缩、隔离、易怒、恐慌、焦虑等，或者社交、睡眠、饮食、学习、工作习惯发生改变。</li>
	<li>有严重的绝望感或无助感。</li>
	<li>陷入经历过的躯体、心理或性虐待中不能自拔。</li>
	<li>有愤怒、攻击性、孤独、内疚、敌意、悲伤或失望等情感表达。</li>
</ul>

<p><b>值得注意的是，自残并不一定是自杀的征兆，虽然自残和自杀有一些相同的原因，比如：</b></p>
<ul>
	<li>逃避难以承受的痛苦。</li>
	<li>改变他人对自己的看法。</li>
	<li>缓解紧张、焦躁的情绪。</li>
	<li>寻求他人的帮助。</li>
</ul>

<p><b>任何人都可能会产生自杀的念头，如果你认为某人有自杀倾向，你应该直接询问那个人，比如：</b></p>
<ul>
	<li>你有自杀的想法吗？</li>
	<li>你想自杀吗？</li>
</ul>

<p><b>切记，不能以高人一等的态度或者命令式的口气，也不应该有先入为主的倾向。像“你是不是想做傻事呀？”这样的表述比较平易近人，有自杀想法的人可能不会过于排斥。</b></p>
<p><b>有些人可能会觉得，这种直接询问的做法会强化对方自杀的想法。但研究表明，如果一个人有自杀倾向，直接询问不会增加他们的自杀风险。相反，这样问实际上给了他们一个解决问题的机会，并让他们知道有人在关心他们。</b></p>
<p><b>当有人告诉你他想自杀的时候，你可能会感到震惊和焦虑，这是可以理解的。但你需要避免做出消极的反应，而是要表现出冷静、自信和富有同情心的样子。</b></p>

<p><b>你可以使用的交谈技巧包括：</b></p>
<ul>
	<li><b>不要回避使用“自杀”一词，直接讨论这个问题是很重要的。</b></li>
	<li>说一些真正关心对方的话比说你应该怎样做更有效。你需要理解、支持有自杀想法的人，还需要全神贯注地倾听他们的话。</li>
	<li>问一问他们在想什么，有什么感受。</li>
	<li>你要让他们安心，向他们表示你愿意听他们说任何想法和感受，以及他们想死的原因。</li>
	<li>让他们知道，可以把这些事情说出来，即使过程可能很痛苦。</li>
	<li>允许他们自由表达自己的情感，比如尽情哭泣、发泄愤怒，甚至是大声咒骂。</li>
	<li>感谢他们愿意分享自己的感受，并告诉他们这样做是有勇气的表现。</li>
	<li>倾听的过程中需要保持耐心和冷静，平静而共情地倾听。</li>
	<li>不轻易同意或者否定他们的态度、行为、观点，让他们自由地表达</li>
	<li>提出开放式问题，这样才能问他们更多问题，从而发现隐藏在自杀念头背后的信息。</li>
	<li>以总结的方式表明你确实在倾听他们，比如，“你想表达的就是……吗？”</li>
	<li>通过对话，找出其中的重要信息，评估危险程度。</li>
	<li>询问既往史（有无自杀未遂）</li>
	<li>分析除自杀之外还可能发生的事情。</li>
	<li>询问其自杀计划。</li>
	<li>时时刻刻表现出同理心，同情并给予支持。</li>
</ul>

<p><b>例如，危机干预中你可以说的语句：</b></p>
<ul>
	<li>对于发生在你身上的事情，我觉得很难过</li>
	<li>你现在安全了。（如果这个人确实是安全的）</li>
	<li>我很高兴现在你和我一起在这里/你正在和我说话。</li>
	<li>这不是你的错。</li>
	<li>你的反应是遇到不寻常事件的正常反应。</li>
	<li>你有这样的感觉是正常的。</li>
	<li>看到/听到/感受到这些一定让人难过/痛苦。</li>
	<li>你不会发疯的。</li>
	<li>事情不会一直这样的，它会变得更好，而你也可以变得更好。</li>
	<li>你的想象会造成一个比事情原本的样子更加可怕的现实。</li>
	<li>哭泣，憎恨，想报复都是没关系的。</li>
</ul>

<p><b>你应该做的事情：</b></p>
<ul>
	<li>达成协议以拖延时间。</li>
	<li>参与其他支持（如家庭干预，经济支持）。</li>
	<li>如可能，转移自杀工具。</li>
	<li>采取行动，告知他人，得到帮助（如果没有能力独立干预，可以将对方危机干预的任务转介给其他人）</li>
	<li>如果自杀风险很高则始终陪伴当事人（如条件允许，现实陪伴效果最佳，退而求次则网络陪伴）</li>
	<li>如果自杀风险很高，切勿让其独处。</li>
</ul>

<p><b>尽管交谈很重要，但仍有很多事情是不适合说或者做的，比如：</b></p>
<ul>
	<li>与他就自杀的想法进行辩论，进行空洞的说教。</li>
	<li>与他讨论自杀是对还是错。</li>
	<li>用愧疚感或威胁来阻止他自杀（比如，告诉他这样做会毁掉他的家庭等）。</li>
	<li>弱化他提出的问题，对事态严重性的认识不足，认为当事人的问题微不足道。</li>
	<li>频繁并且单一的给对方敷衍的安慰。比如，“别担心”，“振作起来”，“你什么都有了”或者“一切都会好起来”。</li>
	<li>用你自己的故事来打断他的话。</li>
	<li>通过肢体语言表达你对某件事不感兴趣或态度消极。</li>
	<li>试图给他做精神疾病诊断。</li>
	<li>对对方诉说的事情表现出震惊、困窘、惊恐。</li>
	<li>刺激当事人加速实行自杀。</li>
	<li>
		给予对方虚假的承诺，例如：<br/>
		承诺对方实施自杀行为时，绝对不会报警。<br/>
		承诺自己会给予对方自己无法给予的帮助。
	</li>
	<li>当对方自杀风险很高的时候，现实中选择离开当事人让其独处。</li>
</ul>

<p><b>同时，危机干预中你不可以说的语句：</b></p>
<ul>
	<li>我知道你的感觉是什么/我了解（应当直接完整复述对方遇到的困难和想法，达成共情，而不应当省略）。</li>
	<li>不会有事的，所有的事情都会没有问题。（这句话无法消除对方内心的恐慌与恐惧）</li>
	<li>你不应该有这种感觉。（认为当事人的困难微不足道）</li>
</ul>

<p><b>当对方重要的人离世时，你不应该说：</b></p>
<ul>
	<li>你能活下来就是幸运的了</li>
	<li>你能救回些东西算是幸运的了</li>
	<li>你是幸运的，你还有其他孩子/亲人/朋友。</li>
	<li>你爱的人在死时并没有受到太多痛苦。</li>
	<li>他/她死之前已经过了幸福的一生。</li>
	<li>他/她现在去了更好的地方/更快乐了。</li>
	<li>你会走过来的，时间会治疗一切的创伤。</li>
	<li>你应该回到你的生活中正常生活下去。</li>
</ul>

<h1>网络自杀干预注意事项：</h1>
<h2>一、报警怎么报？</h2>
<p>
	<b>1.电话报警</b>，拨110陈述事实之后，民警会告诉你要报给自杀者所在地的公安，直接拨110拨到的只是本地的；拨给自杀者所在地公安应该是110前加区号，各地区号现在网络上也是能查询到了。例如北京110则是拨打010110<br/><br/>
	报警后向民警提供对方你知道的所有相关信息（如果一个信息无法确定真实性则不提供），如身份证号，身份证姓名，身份证性别，电话号码，住址，社交关系，亲属联系方式，微信号，QQ号，工作单位，学校等，有多少信息给多少信息。<br/><br/>
	如果是跨性别者则向警察额外说明对方的生理性别，并且把对方打扮成了另一个性别的情况说明，例如：这是个男孩，但是喜欢把自己打扮成女孩。（危急时刻不要和民警纠结跨性别的概念！）<br/><br/>
	即使只有一个QQ号或者微信号也可以向民警报警，在警察面前，所有的网络信息都是透明的。但是需要注意的是，信息越少民警找人花费的时间就越久甚至可能放弃找人，所以一般情况下信息越全越好。<br/><br/>
	（极少部分情况下）如果民警质问：只有一个QQ号/微信号不知道地址姓名我们怎么找人？则可以向对方表示要求对方使用数据手段进行干预，和对方说“我已经和本地公安报过警，对方说只需要提供QQ/微信号就可以了”。
</p>
<p>
	<b>2.微信报警</b>，现在各地区都有自己的公安微信公众号，可以直接在公众号（注意是自杀者所在地的公安微信公众号）上报警，要填写相关信息。最好把宣告自杀的帖子截图下来，在提供图片那一栏里发上去；电话号码也会要求提供（往往是需要短信验证码验证后才能报案）<br/><br/>
	微信报警之后不久，就会接到警察的来电，详细核实所给的信息，但是也会问你的包括身份证号在内的个人信息！<br/><br/>
	出警到地方之后一般会再打一通核实一遍相关信息。
</p>

<h2>二、报警的后果</h2>
<p>来自自杀者的麻烦：报警只能救到这一次，若是无法提供后续的心理治疗服务，则还是会不断的再次自杀，同时一个人自杀未遂的次数越多，那TA下一次自杀成功的概率和尝试自杀的频率往往也会越来越高。</p>
<p>由于没得到相关心理治疗，自杀者往往会忌恨报警者（以及警察）。</p>

<h2>三、什么时候应该报警</h2>
<p>报警具有极大的风险性，不到非常危机的时刻或者是恰当的情况下不要轻易报警，否则极有可能适得其反。</p>
<ol>
	<li>当对方表现出极为明确的即将实施自杀行为的想法，又失联的情况下；<br/>例如QQ/微信信息不回复，电话不接，斟酌实际情况报警。</li>
	<li>当对方已经实施自杀行为时必须报警。</li>
	<li>当对方正在遭遇严重侵害时（如家庭矛盾），斟酌实际情况报警。</li>
	<li>当对方正在实行高人身安全风险行为时，斟酌实际情况报警。</li>
</ol>

