<p>是否休学或辞职，应该从我们自身的病情考虑，疾病的严重程度是最重要的判定因素。抛开疾病的严重程度谈休学或辞职，都是对病情、对自己、对未来不负责任的表现。<p>

<h2><b>第一，要积极治疗。</b></h2>
<p>血的事实告诉我们，抑郁症必须治疗。许多患者的亲朋好友认为，抑郁症只是心理、情感问题，只要谈谈话，疏导疏导，“打开心结”，就能“走出来”。这实在是不懂科学的表现。</p>
<p>因为研究显示，抑郁症是一种生理性的疾病，尤其是中度、重度抑郁症患者，绝大多数已经产生了脑部病变。</p>
<p>至于患者本人，得了抑郁症，起先不自知；捱了很多时日，才会犹犹豫豫走上求治之路。那时他心理准备不足，当医生告诉他，疗程很长，至少半年以上，甚至两年、三年，还要不断复诊、复查，他就会畏难、绝望、抗拒，经常不能坚持治疗，最终酿成悲剧。</p>
<p>一旦发现自己得了抑郁症，不要犹豫，立刻去正规的精神科医院；根据自己的病情程度，请医生决定，是吃药，还是看心理医生。不要浪费宝贵的时间。在抑郁症早期，时间就是生命。</p>

<h2><b>第二，干预抑郁症患者自杀，最关键的是判断患者何时最有可能自杀。</b></h2>
<p>自杀分三个步骤：自杀意念、自杀企图、自杀实施。几乎每个抑郁症患者都会有意念和企图，但要走到实施这一步，还需要客观条件。</p>
<p>依据抑郁症临床症状表现，医学上把抑郁症分类为轻度、中度和重度三种类型。</p>
<p>轻度抑郁症患者心境低落，兴趣和愉快感丧失，容易疲劳，多思多虑，自卑消极，无缘无故出现多种躯体不适；</p>
<p>到了中度，还会追加脑功能阻滞和精神运动性阻滞，患者感到自己大脑思维功能、行动功能和社会功能下降，不敢见人，人际交往发生障碍；</p>
<p>到了重度，患者情绪极为抑郁，已无法感知喜怒哀乐，思维动作严重迟缓，语速慢，语音低，语量少，应答迟钝，严重者可呈木僵状态。一天之内，经常不言不语，不动不吃。</p>
<p>那么，是不是重度抑郁症自杀危险最大？</p>
<p>不是。这正是抑郁症的独特之处：抑郁症患者自杀，往往发生在从轻度向中度恶化，以及从重度向中度好转的阶段。真正的重度抑郁症患者自杀的概率相对较低。</p>
<p>原因是，抑郁症药物治疗的特点是：先改善患者的动力，后改善患者的情绪。自杀要具备两个条件，即自杀的意愿和执行的动力。</p>
<p>重度患者往往大脑一片空白、体力不支，不具备自杀能力；而药物一旦起效，患者大脑的抑制先得以解除（有了动力），可是情绪的好转要落后一周（自杀意念还在），自杀往往在这一阶段发生。</p>
<p>这也是为何，部分中度和重度抑郁症患者在接受药物治疗以后，在数周内自杀率反而上升。</p>
<p>也许患者再坚持一两天，就能挣脱黑暗，迎来光明。但是，他看不见曙光在前，放弃在最后一刻，功亏一篑。</p>
<p>痛苦是很难杀人的，绝望才是真正的杀人元凶。</p>
<p>干预自杀，就要学会识别患者从轻度向中度恶化、尤其是从重度向中度好转的关口。在这两个时段，要把病人看好，最好寸步不离。一旦出现闪失，后悔莫及！</p>
<p>如果病人吃药一个月，一直情绪状态没有好转，但是忽然开始闹事，拒绝吃药，情绪激动</p>
<p>这说明患者有力气闹，可能药要见效了。如果病人闹，由他闹，不用劝解，只需做好两件事：一是督促他每天吃药，一粒不能少；二是把家里的阳台、窗户封好，寸步不离人。</p>
<p>意志比较坚强的患者，则要有自我拯救意识。自杀往往发生在一念之间，很多时候仅靠意志难以抵抗自杀的冲动，这时，就要有意识地让自己不具备自杀的条件。只要死不那么容易，自杀冲动就会再而衰、三而竭。</p>
<p>一般来讲，可以根据以下两种疾病状态来判断在现阶段休学或辞职是否合适。</p>
<p>如果是轻中度抑郁，你可能会有失眠或嗜睡等睡眠障碍，以及身体疲惫、缺乏精力、肠胃不适、头晕、头痛等常见的躯体化反应。这种情况下我们可以偶尔放松一下，远离学业或者工作的压力，比如给自己放一个小假休息一段时间。但这个时间一般不能超过三个月，如果超过三个月甚至达到半年之久，那就不是一次短暂的放松，而是在逃避。</p>
<p>如果是重度抑郁，你是没办法继续完成正常的学业和工作的，尤其是已经出现了一些木僵（身体僵住不能动）、妄想、幻觉的时候，不仅无法正常上学和工作，甚至还会出现一些伤害自己或他人的危险想法。</p>
<p>所以一旦确诊是重度抑郁，我们最需要做的是告诉家人，让他们帮自己做好保护，或者直接住院治疗，控制住病情，确保病情不会进一步恶化。</p>

<h2><b>第三、当一个人因为自杀去世时，切忌在公开互联网中传播ta的自杀方法</b></h2>
<p>一个人自杀死亡后，如果电视新闻详细报道了这个人的自杀方法，就会立刻导致接下来一段时间周围收视地区自杀率大幅度上升，并且自杀的方法都会模仿这个已经成功的案例</p>
<p>这也是为什么所有的传媒信息都不会讲人的自杀方法，甚至不会讲怎么死的</p>

