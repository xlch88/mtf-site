<h2>尾言：</h2>
<p>本文章由寒涟漪结合大量参考文献进行整理。</p>
<p>这篇文章我其实写的非常非常的保守，文章所列出的所有的方法几乎都是没有任何法律风险以及对干预人本身造成伤害的风险的。通篇大量采用了已有多文献方法和普适方法。同时也回避了大量社群中存在的一些问题的干预方法。同时对于大量效果存疑/有争议/有人身安全风险/有法律风险的干预手段我都没有列出。</p>
<p>我其实在好几年前，就一直有想整理一个在跨性别社群中有力并且有效的危机干预手册了。但是说实话，直到今天为止，我也不知道具体什么方法才是社群干预最优的手段，如果一味的去照搬心理学和危机干预学的文献，就会存在严重水土不服的问题。但如果单方面的拿我自身的干预经历去总结，又极有可能产生严重的误导。</p>
<p>因为我在很早的时候就发现，绝大多数的危机干预文献在干预中国大陆地区的跨性别群体时都存在严重的水土不服的问题，尤其是在除了心理干预（谈话交流）以外的具体社会工作方面，这导致我曾经经历了大量的失败，甚至一度陷入“反文献，反权威”的极端境地中。</p>
<p>实际上，在我整理关于危机干预的相关文献时期发现，大量的危机干预文献有很多都是自相矛盾的。</p>
<p>有的文献鼓励你多说“一切都会好起来”。</p>
<p>有的文献则认为“一切都会好起来”属于敷衍且空洞的安慰，不能说。</p>
<p>有的文献认为在处理对方亲密的人离世时应该说“这不是你的错”。</p>
<p>有的文献则认为“这不是你的错”会加重对方的悲伤。</p>
<p>有的文献认为应当在询问对方“你有自杀的想法吗”的基础上进一步和对方讨论自杀方法。</p>
<p>有的文献则认为这是禁忌，讨论自杀方法会大幅度增加对方的自杀成功率。</p>
<p>有的文献认为讨论自杀方法可以进行错误的引导，起到增加自杀难度和拖延时间的目的来增加自杀干预的成功率。</p>
<p>有的文献则认为不应当对当事人进行任何意义上的知识类方面的欺骗，会有巨大风险。</p>
<p>这还仅仅是自杀干预方面的关于谈话交流的巨大争议点，关于实际的现实中的社会工作方法更是出现大量自相矛盾的内容。</p>
<p>并且这些自相矛盾的内容同时出现在不同的权威著作上。</p>
<p>但是到今天为止我仍然没有找到除了实践以外的最佳方法，而实践，是以人命为代价的。</p>
<p>但显然，整理指导手册是必须的，社群危机干预指导手册的意义其实非常的简单粗暴：有总比没有好。</p>
<p>就好像社群中很多不合格的干预者：贡献大于伤害，就是好的。</p>
<p>我早在很多年前就深深的意识到，只靠一个人的力量是永远不够的，只靠一个人，社群现状永远无法改变；永远都会有新的花瓣凋零。</p>
<p>哪怕手册的内容不全面，受到各种局限性，但只要对社群中的干预者起到一点点参考意义，都是有价值的。</p>
<p>同时我也能切身体会到绝大多数志愿者面临的窘境：有一颗想要帮人的心，却不知道从何做起，又不知道怎么做才能合适的，于是黯然退出。</p>
<p>希望以后这种情况会越来越少。</p>