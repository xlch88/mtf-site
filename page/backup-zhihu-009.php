<p>对于多元性别的讨论经常产生逻辑滑坡，仿佛允许跨性别接受性别肯定手术就会让所有青少年去随便“变性”，一个人对自己的性别认同或许会产生摇摆、困惑，但这样的探索过程实际上是TA确立自身认同的关键一步，并不是儿戏。</p>
<p>有研究表明，成年后接受性别肯定手术的跨性别者，最早在7岁就开始经历性别不安【3】，而青春期第二性征的发育可能会加剧TA们的焦虑，严重的甚至会导致抑郁等心理状况的产生。</p>
<div class="block">
	但从科学研究和统计得到的数据来看，“未成年人还不成熟，性别这种人生大事要等到成年以后有判断能力以后再决定”的思维如果贯彻应用到现实中，将会导致更多的跨性别青少年因为自杀而死亡，也会让更多的成年跨性别者因为因为承受了更大的压力，所以有更多的出现疾病的风险，例如压力比较大的人容易肥胖、容易诱发精神疾病、糖尿病高血压等。<br/>
	首先需要明确的第一点科学事实是：绝大多数性别焦虑持续时间超过两年以上的跨性别青少年，终生都会保持其性别认同（终生不会后悔）。<br/>
	按照美国ucsf的跨性别中心的跨性别青少年的报告数据显示：当一个跨性别青少年性别焦虑的持续时间达到2年以上时，其在接下来终生的时间内不会后悔的人数比例在80％以上，如果性别焦虑持续时间达到3/4/5年，其接下来终生时间不会后悔的比例在90％/97％/99％以上，并且即使在极少数的情况下出现后悔情况，其对人生生活质量水平，心理健康，死亡率的负面影响也远远小于一个终生不会后悔的跨性别者无法得到及时治疗的负面影响。<br/>
	如果一个跨性别者要等到成年以后才进行干预治疗，其终生将会面临比一般人群高出数百倍的自杀风险和抑郁风险，并且因为生理原因面临严重的性别焦虑。<br/>
	其次需要明确的第二点科学事实是：在一个青少年进入青春期以后（一般在11岁以后），性别认同一旦形成，无法因为任何强制性的外力原因而改变，如果对其进行“矫正治疗”，在99.7％以上的情况下不仅无法成功“矫正”，而且会加重其性别焦虑，导致在86.1％以上的情况下让该青少年患上/加重精神疾病，从而让该青少年的自杀率比普通人群高出700倍以上。
</div>

<p>正规医疗机构在开展性别肯定护理时会使用青春期阻滞剂，相关药物会在青春期抑制青少年的性激素，从而帮助跨性别青少年降低抑郁和焦虑，青春期阻滞剂作为一种可逆的手段（可以后悔），为跨性别青少年提供了更深刻地探索自身性别认同的时间。</p>
<div class="block">
	医学上已经为了防止“后悔”的情况给出了有效的干预手段，那就是“青春期阻滞剂”，该阻滞剂的效果是暂缓跨性别者青春期的第二性征发育，从而达到缓解性别焦虑，降低自杀风险，给出犹豫时间的目的，这样即使孩子后悔了，只要停止服用“青春期阻滞剂”，会依然按照原来的生理性别发育。
</div>
<p>来自荷兰的一项对跨性别青少年长期随访的研究指出，在接受性激素治疗和性别肯定手术后，跨性别青少年的幸福感与同龄人报告的相似或更好，并且没有一个研究参与者后悔进行治疗【4】。</p>
<p>在国内，2021年，我国首个跨性别儿童与青少年多学科门诊就在上海成立。在《中国跨性别群体生存现状调查报告》《中国同志心理健康调研报告》等调查研究中，也都有相当多数量的跨性别青少年表达了自己所遭遇的困境。43.7%的家长完全不能接受孩子的跨性别身份，这种不接受同时伴随着多种形式的家庭暴力，与其说“跨性别青少年”不存在，不如说是社会与家庭选择性地忽视了TA们的存在。</p>
