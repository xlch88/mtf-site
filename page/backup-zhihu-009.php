<p>但是在中国大陆，据2017年发布的《中国跨性别群体生存现状调查报告》显示，TA们当中能获得相关医疗服务的却很少，在该调查所涉及的样本中，仅有少数人（14.8%）进行过性别肯定手术。进行手术同时也要面对来自家庭、医疗系统的各方阻碍。</p>
<p><b>需要注意的是：阻止跨性别者获得正规的治疗可能会导致许多跨性别者尝试自行切除生殖器官而造成生命危险；在中国大陆地区每年都有大量的跨性别者自行切除生殖器官的案例。</b></p>
<p>而国外有研究统计了跨性别群体接受性别肯定手术后的后悔率，这一后悔率在跨性别者群体<b>仅占不到1％</b>［1］（且该后悔率还是基于发达国家的性别肯定手术医疗门槛远低于中国大陆地区的基础上的，可推断在中国大陆地区该后悔率更加低得多）。且在后悔的群体中，绝大多数后悔的原因是<b>因手术而与伴侣产生矛盾；术后不被社会、亲属接受；对完成手术流程等待中的不满意；术后意识到自己是非二元性别、仍然会对目前的身体感到性别不安等。</b></p>
<p>在其中，性别肯定手术后非常严重的疼痛也是重要的因素之一。</p>
<p>可见，接受性别肯定手术后后悔并不等于TA不再认同自己是跨性别，也有可能是因为对手术流程、结果的不满意。</p>
<p>在由跨儿心理支持的一项中国本土小范围调研也指出，跨性别群体在接受性别肯定手术后，由于需要面对康复期的生理痛苦，也会在术后感到一定程度的困扰【2】。</p>
<p>而降低这种后悔概率的方法非常众多：</p>
<p>加强对激素使用、性别肯定手术的科普，让大家更完备地意识到自己的身心可能产生的变化及其影响。也应该完善与接受性别肯定手术后的跨性别群体相关的社会保障、法律法规（如改证、恢复期的医疗支持、心理支持等），这都能够尽可能减少后悔而带来的二次伤害。</p>
<p>医学过渡的目的并不是把人从一个性别刻板印象的模子里拿出来、放进另一个模子，<b>而是为了让帮助其更好地与自我融洽、更好地融入社会。</b>跨性别群体的医疗需求始终存在，一个更好的选择是正视这些需求、并完善与其相关的规章制度，而不是因为可能存在的后悔案例而因噎废食，忽视所有跨性别群体的需求。</p>

