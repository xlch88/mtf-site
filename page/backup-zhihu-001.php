<p>非常遗憾的是，我可以说在未成年人的救助政策方面，国内的社会保障机制是严重缺位的。</p>
<p>对于被父母完全遗弃的孩子，尚且可以通过法律援助和法律诉讼的形式向父母索要赡养费和住进福利院的待遇，但这其中的成本近乎高到无法接受的地步。孩子本人几乎很难有这样的执行力去进行，而对于帮助孩子的志愿者来说，法律诉讼的成本甚至要比志愿者自己把孩子养到18岁的成本还要更高。</p>
<p>而对于家庭暴力的个案就更加变得难以干预了，我有一位曾在澳洲留学社工专业，回国后尝试过做家庭暴力干预的志愿者朋友，她在做家暴个案的过程中遇到了重重阻挠，其中警察、律师、民政机构、当事人父母，甚至法官都会质疑她为何要干涉别人的“家事”，近乎困难重重，无法进行。最终只能通过把孩子强行从原生家庭接出来这种非法的方法来做家暴的紧急避险。</p>
<p>并且对于我下文所叙述的这位女孩的情况，我和她近乎是把所有的政府部门和民间部门都求助了个遍，也是困难重重，难以开展。</p>

