<p>一年后我们分开了，但这个礼物，我至今珍藏<br/></p>

<p>而在她以后，我也就很少再有过这么一段稳定、漫长、专一、并且能有机会在现实中经常黏在一起的恋爱经历了；而在以后的我看来，那一段时光真的是如梦一样的经历，这段恋情也在我自己最需要温暖的时候，得到了陪伴</p>
<p>讽刺的却是，在这段恋情以后，后来和我有恋爱经历的对象精神问题一个比一个严重，数量也越来越多；而亲密关系本质上又常常是对抑郁症患者来说的一剂毒药</p>
<p>一方面的原因是心理问题越严重的人我往往越会主动去靠近对方；另一方面的原因是那个时期的我心智不成熟，年龄也还远远没有成年</p>
<p>在缺乏专业干预知识并且求助无门的情况下，除了带对方看病吃药住院以外，我也只能想到以包括恋爱在内的各种非正常的方式去改善对方的心理状态；在长时间的相处下我也常常会对对方产生复杂的感情。于是常常有很多关系亲密的朋友相处的时间久了以后就会暧昧不清，最终慌然答应</p>
<p>而我的恋爱模式也渐渐发展成了polyamory，能分摊在每一个人身上的时间越来越少，生活也变得越来越忙，我也再也不可能像我最开始的时候那样，能够整日整夜的除了学业以外和日思夜想的人黏在一起</p>
<p>但对我自己来说，尽管耳边殇悲的话已是日常，尽管在梦中也会对爱人的安危惊心胆战，尽管每天生活就像是打仗一样</p>
<p>但回头一看，我从来没有后悔过自己的选择，也从来没有忘记我遭遇过的最初的殇悲与愿望，梦境深处也仍然会倒映着那个永远离开了我的那个女孩的音容笑貌</p>
<p>也永远记得在我孩童时最孤独的时候得到那温暖陪伴时是什么样的感觉</p>


